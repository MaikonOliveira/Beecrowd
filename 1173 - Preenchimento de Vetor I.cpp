#include <iostream>

using namespace std;

int main (){
    int vetor[10],i,valor;
    cin>>valor;
    if(valor<=50){
        for(i=0;i<10;i++){
            vetor[i]=valor;
            valor+=valor;
            cout<<"N["<<i<<"] = "<<vetor[i]<<endl;
        }
    }
}
