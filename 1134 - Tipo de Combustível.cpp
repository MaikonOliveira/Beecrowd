#include <iostream>

using namespace std;

int main(){
    int gasolina=0, alcool=0, diesel=0, valor;
    cin>>valor;
    do{
        switch(valor){
            case 1:
                alcool++;
                cin>>valor;
                break;
            case 2:
                gasolina++;
                cin>>valor;
                break;
            case 3:
                diesel++;
                cin>>valor;
                break;
            case 4:
                break;
            default:
                cin>>valor;
                break;
        }
    }while(valor!=4);
    cout<<"MUITO OBRIGADO"<<endl<<"Alcool: "<<alcool<<endl<<"Gasolina: "<<gasolina<<endl<<"Diesel: "<<diesel<<endl;
}
